def replace_section(lines: list[str], header: str, new_block: list[str]) -> list[str]:
    start = None
    for i, line in enumerate(lines):
        if line.strip() == header:
            start = i
            break
    if start is None:
        raise RuntimeError(f"Header not found: {header}")

    end = len(lines)
    for j in range(start + 1, len(lines)):
        if lines[j].startswith("10.") and lines[j].strip() != header:
            end = j
            break

    # Ensure block ends with a blank line for readability
    if new_block and new_block[-1] != "":
        new_block = new_block + [""]
    return lines[:start] + new_block + lines[end:]


def main() -> None:
    path = Path("01-rulebook.md")
    raw = path.read_text(encoding="utf-8")
    lines = raw.splitlines()

    sections = build_sections()
    for header, block in sections.items():
        lines = replace_section(lines, header, block)

    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


if __name__ == "__main__":
    main()

